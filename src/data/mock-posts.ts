function generatePost(template: PostTemplate, index: number): Post {
  const author = mockUsers[index % mockUsers.length];
  const gradient = gradients[index % gradients.length];
  const aspect = aspects[index % aspects.length];
  const daysAgo = Math.floor(Math.random() * 30) + 1;
  const date = new Date();
  date.setDate(date.getDate() - daysAgo);

  return {
    id: `post-${index + 1}`,
    type: template.type || "image-text",
    sectionId: template.sectionId,
    author: {
      id: author.id,
      name: author.name,
      avatarColor: author.avatarColor,
      avatarInitial: author.avatarInitial,
    },
    title: template.title,
    summary: template.summary,
    content: template.content,
    coverGradient: gradient,
    coverAspectRatio: aspect,
    images: [gradient],
    tags: template.tags,
    stats: {
      likes: Math.floor(Math.random() * 2000) + 100,
      comments: Math.floor(Math.random() * 200) + 10,
      bookmarks: Math.floor(Math.random() * 800) + 50,
      shares: Math.floor(Math.random() * 100) + 5,
    },
    isHot: template.isHot || false,
    isFeatured: template.isFeatured || false,
    createdAt: date.toISOString(),
    poll: template.poll,
  };
}

export const mockPosts: Post[] = templates.map((t, i) => generatePost(t, i));

export function getPostsBySection(sectionId: SectionId): Post[] {
  return mockPosts.filter((p) => p.sectionId === sectionId);
}

export function getPostById(id: string): Post | undefined {
  return mockPosts.find((p) => p.id === id);
}

export function getHotPosts(): Post[] {
  return mockPosts.filter((p) => p.isHot);
}

export function getFeaturedPosts(): Post[] {
  return mockPosts.filter((p) => p.isFeatured);
}
